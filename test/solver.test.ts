import * as chai from "chai";
const { expect } = chai;

import { solve } from "../src/solver";

describe("solver", () => {
  it("should solve a puzzle", () => {
    const data = [
      [5, 3, 0, 0, 7, 0, 0, 0, 0],
      [6, 0, 0, 1, 9, 5, 0, 0, 0],
      [0, 9, 8, 0, 0, 0, 0, 6, 0],
      [8, 0, 0, 0, 6, 0, 0, 0, 3],
      [4, 0, 0, 8, 0, 3, 0, 0, 1],
      [7, 0, 0, 0, 2, 0, 0, 0, 6],
      [0, 6, 0, 0, 0, 0, 2, 8, 0],
      [0, 0, 0, 4, 1, 9, 0, 0, 5],
      [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ];
    const solved = solve(data);
    expect(solved).to.equal(true);
    expect(data).to.deep.equal([
      [5, 3, 1, 2, 7, 4, 6, 0, 8],
      [6, 7, 4, 1, 9, 5, 3, 0, 0],
      [0, 9, 8, 3, 5, 0, 1, 6, 2],
      [8, 1, 2, 7, 6, 0, 4, 9, 3],
      [4, 5, 7, 8, 0, 3, 9, 2, 1],
      [7, 0, 3, 9, 2, 1, 8, 0, 6],
      [1, 6, 9, 5, 4, 7, 2, 8, 0],
      [2, 8, 0, 4, 1, 9, 0, 0, 5],
      [0, 2, 0, 6, 8, 0, 5, 7, 9],
    ]);
  });
});
